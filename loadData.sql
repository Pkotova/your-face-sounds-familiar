-- People over 40
CREATE VIEW PEOPLE_OVER_40
AS
    SELECT PERSON_ID, NAME, NICKNAME, GENDER, AGE, JOB
    FROM PEOPLE
    WHERE AGE > 40;

--Weekly winners
CREATE VIEW WEEKLY_WINNER
AS
    SELECT PARTICIPANT_ID,NAME,EPISODE_ID,SEASON_ID,POINTS
    FROM  RATED_IN , PEOPLE
    WHERE RATED_IN.PARTICIPANT_ID = PEOPLE.PERSON_ID
    AND RATED_IN.POINTS = 10;


CREATE TABLE SEASONS
(
    SEASON_ID  INT         NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    TV_CHANEL  VARCHAR(30) NOT NULL,
    YEAR       INT         NOT NULL,
    EPISODES   INT         NOT NULL,
    START_DATE DATE        NOT NULL,
    FINAL_DATE DATE        NOT NULL,
    AWARD      VARCHAR(32) NOT NULL,
    WINNER_ID  INT         NOT NULL,

    CONSTRAINT PK_SEASON_ID PRIMARY KEY (SEASON_ID),

    FOREIGN KEY (WINNER_ID)
        REFERENCES PARTICIPANTS (PARTICIPANT_ID),

    CHECK (YEAR >= 2013),
    CHECK ( EPISODES >= 12 ),
    CHECK ( FINAL_DATE > START_DATE )
);